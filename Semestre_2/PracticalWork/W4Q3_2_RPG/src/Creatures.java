public class Creatures {
    int strength, agility, health, mana, level, experience;
    String language, name, weapon, race, classe;

    public Creatures(int strength, int agility, int health, int mana, int level,
                     int experience, String language, String name, String weapon, String race, String classe) {
        this.strength = strength;
        this.agility = agility;
        this.health = health;
        this.mana = mana;
        this.level = level;
        this.experience = experience;
        this.language = language;
        this.name = name;
        this.weapon = weapon;
        this.race = race;
        this.classe = classe;
    }

}
